a = [4,2,1,2,0,7,10,3,2,1]

# Menampilkan list index ke-4
print(a[4])     # output = 0

# Menampilkan list index ke-7
print(a[7])     # output = 3

# Menampilkan list index ke-2 s/d 5
print(a[2:5])   # output = [1,2,0]

# Menampilkan list index ke-6 s/d 8
print(a[6:8])   # output = [10,3]

# Menampilkan list dari awal s/d ke-5
print(a[:5])    # output = [4,2,1,2,0]

# Menampilkan list dari awal s/d ke-3
print(a[:3])    # output = [4,2,1]

# Menampilkan list dari index ke-2, ke-4, ke-7, ke-1
print(a[2],a[4],a[7],a[1]) # output = 1 0 3 2

# Menampilkan list index ke-0, ke-4, ke-7, ke-2, ke-6
print(a[0],a[4],a[7],a[2],a[6]) # output = 4 0 3 1 10

# Menampilkan list index ke-3 s/d seterusnya
print(a[3:]) # output = [2, 0, 7, 10, 3, 2, 1]

# Menampilkan list index ke-4 s/d seterusnya
print(a[4:]) # output = [0,7,10,3,2,1]